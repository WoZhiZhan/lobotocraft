    public ExtendedColor(int r, int g, int b) {
        super(r, g, b);
    }

    // ==================== 转换方法 ====================

    /**
     * 将Color对象转换为int数组 [r, g, b]
     * @param color Color对象
     * @return RGB数组，值范围 0-255
     */
    public static int[] toArray(Color color) {
        return new int[]{color.getRed(), color.getGreen(), color.getBlue()};
    }

    /**
     * 将Color对象转换为带透明度的int数组 [r, g, b, a]
     * @param color Color对象
     * @return RGBA数组，值范围 0-255
     */
    public static int[] toArrayWithAlpha(Color color) {
        return new int[]{color.getRed(), color.getGreen(), color.getBlue(), color.getAlpha()};
    }

    /**
     * 从int数组创建Color对象
     * @param rgb RGB数组 [r, g, b]
     * @return Color对象
     */
    public static Color fromArray(int[] rgb) {
        if (rgb.length == 3) {
            return new Color(rgb[0], rgb[1], rgb[2]);
        } else if (rgb.length == 4) {
            return new Color(rgb[0], rgb[1], rgb[2], rgb[3]);
        }
        throw new IllegalArgumentException("Array must have 3 or 4 elements");
    }

    /**
     * 根据十六进制颜色代码创建Color对象
     * @param hex 十六进制颜色代码，例如 "#FF5733" 或 "FF5733"
     * @return Color对象
     */
    public static Color fromHex(String hex) {
        hex = hex.replace("#", "");
        return new Color(
                Integer.parseInt(hex.substring(0, 2), 16),
                Integer.parseInt(hex.substring(2, 4), 16),
                Integer.parseInt(hex.substring(4, 6), 16)
        );
    }

    /**
     * 将Color对象转换为十六进制字符串
     * @param color Color对象
     * @return 十六进制颜色代码，例如 "#FF5733"
     */
    public static String toHex(Color color) {
        return String.format("#%02X%02X%02X", color.getRed(), color.getGreen(), color.getBlue());
    }

    // ==================== 颜色操作方法 ====================

    /**
     * 创建半透明颜色
     * @param color 原始颜色
     * @param alpha 透明度 (0-255)
     * @return 带透明度的新Color对象
     */
    public static Color withAlpha(Color color, int alpha) {
        return new Color(color.getRed(), color.getGreen(), color.getBlue(), alpha);
    }

    /**
     * 使颜色变亮
     * @param color 原始颜色
     * @param factor 变亮系数 (0.0-1.0)
     * @return 变亮后的颜色
     */
    public static Color lighter(Color color, float factor) {
        int r = Math.min(255, (int)(color.getRed() * (1 + factor)));
        int g = Math.min(255, (int)(color.getGreen() * (1 + factor)));
        int b = Math.min(255, (int)(color.getBlue() * (1 + factor)));
        return new Color(r, g, b);
    }

    /**
     * 使颜色变暗
     * @param color 原始颜色
     * @param factor 变暗系数 (0.0-1.0)
     * @return 变暗后的颜色
     */
    public static Color darker(Color color, float factor) {
        int r = Math.max(0, (int)(color.getRed() * (1 - factor)));
        int g = Math.max(0, (int)(color.getGreen() * (1 - factor)));
        int b = Math.max(0, (int)(color.getBlue() * (1 - factor)));
        return new Color(r, g, b);
    }

    /**
     * 混合两种颜色
     * @param color1 颜色1
     * @param color2 颜色2
     * @param ratio 混合比例 [0, 1]，0为完全是color1，1为完全是color2
     * @return 混合后的颜色
     */
    public static Color mix(Color color1, Color color2, float ratio) {
        int r = (int)(color1.getRed() * (1 - ratio) + color2.getRed() * ratio);
        int g = (int)(color1.getGreen() * (1 - ratio) + color2.getGreen() * ratio);
        int b = (int)(color1.getBlue() * (1 - ratio) + color2.getBlue() * ratio);
        return new Color(r, g, b);
    }

    /**
     * 创建颜色的副本
     * @param color 原始颜色
     * @return 颜色副本
     */
    public static Color copy(Color color) {
        return new Color(color.getRed(), color.getGreen(), color.getBlue(), color.getAlpha());
    }

    /**
     * 反转颜色
     * @param color 原始颜色
     * @return 反转后的颜色
     */
    public static Color invert(Color color) {
        return new Color(
                255 - color.getRed(),
                255 - color.getGreen(),
                255 - color.getBlue()
        );
    }

    /**
     * 将颜色转换为灰度
     * @param color 原始颜色
     * @return 灰度颜色
     */
    public static Color toGrayscale(Color color) {
        int gray = (int)(color.getRed() * 0.299 + color.getGreen() * 0.587 + color.getBlue() * 0.114);
        return new Color(gray, gray, gray);
    }

    /**
     * 增加颜色饱和度
     * @param color 原始颜色
     * @param amount 增加量 (0.0-1.0)
     * @return 高饱和度颜色
     */
    public static Color saturate(Color color, float amount) {
        float[] hsb = Color.RGBtoHSB(color.getRed(), color.getGreen(), color.getBlue(), null);
        hsb[1] = Math.min(1.0f, hsb[1] + amount);
        return Color.getHSBColor(hsb[0], hsb[1], hsb[2]);
    }

    /**
     * 降低颜色饱和度
     * @param color 原始颜色
     * @param amount 降低量 (0.0-1.0)
     * @return 低饱和度颜色
     */
    public static Color desaturate(Color color, float amount) {
        float[] hsb = Color.RGBtoHSB(color.getRed(), color.getGreen(), color.getBlue(), null);
        hsb[1] = Math.max(0.0f, hsb[1] - amount);
        return Color.getHSBColor(hsb[0], hsb[1], hsb[2]);
    }

    /**
     * 创建彩虹色
     * @param hue 色相 [0, 1]
     * @return 彩虹色
     */
    public static Color rainbow(float hue) {
        return Color.getHSBColor(hue % 1.0f, 1.0f, 1.0f);
    }

    /**
     * 创建彩虹色（带自定义饱和度和亮度）
     * @param hue 色相 [0, 1]
     * @param saturation 饱和度 [0, 1]
     * @param brightness 亮度 [0, 1]
     * @return 彩虹色
     */
    public static Color rainbow(float hue, float saturation, float brightness) {
        return Color.getHSBColor(hue % 1.0f, saturation, brightness);
    }

    /**
     * 根据时间创建动态彩虹色
     * @param time 时间（通常是System.currentTimeMillis() / 1000）
     * @param speed 变化速度
     * @return 动态彩虹色
     */
    public static Color animatedRainbow(float time, float speed) {
        return rainbow((time * speed) % 1.0f);
    }
